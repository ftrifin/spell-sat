#ifndef __SPELL_WS_OBJECT_DATAHANDLER_H__
#define __SPELL_WS_OBJECT_DATAHANDLER_H__

// FILES TO INCLUDE ////////////////////////////////////////////////////////
// Local includes ----------------------------------------------------------
#include "SPELL_WS/SPELLwsDataHandler.H"
// Project includes --------------------------------------------------------
// System includes ---------------------------------------------------------

/** \addtogroup SPELL_WS */
/*@{*/

// FORWARD REFERENCES //////////////////////////////////////////////////////
// ENUMS ///////////////////////////////////////////////////////////////////
// TYPES ///////////////////////////////////////////////////////////////////
// DEFINES /////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Data handler for Python objects
 **
 ** \par Description and usage:
 **
 **		Data handlers are classes that deal with Python object read/write
 **		and serialization processes, used for writing or reading ws files.
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLwsObjectDataHandler : public SPELLwsDataHandler
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		 *
		 * \param object IN: the associated Python object
		**////////////////////////////////////////////////////////////////////
		SPELLwsObjectDataHandler( PyObject* object );

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		virtual ~SPELLwsObjectDataHandler();

    // METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLwsDataHandler.
		**////////////////////////////////////////////////////////////////////
		virtual void write();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLwsDataHandler.
		**////////////////////////////////////////////////////////////////////
		virtual void read();

		//////////////////////////////////////////////////////////////////////
		/** Get the associated Python object.
		 *
		 * \return The associated Python object.
		**////////////////////////////////////////////////////////////////////
		PyObject* getObject() const
		{
			return m_object;
		};

protected: //-----------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Set the associated Python object.
		 *
		 * \param object IN: the associated Python object.
		**////////////////////////////////////////////////////////////////////
		void setObject( PyObject* object )
		{
			m_object = object;
		};

		//////////////////////////////////////////////////////////////////////
		/** Store a fake object
		 *
		 * \param code IN: data code for the object
		**////////////////////////////////////////////////////////////////////
		void storeFakeObject( SPELLwsDataCode code );

	// DATA MEMBERS //////////////////////////////////////////////////////////

private: /////////////////////////////////////////////////////////////////////

	// EXCEPTIONS ////////////////////////////////////////////////////////////
	// ENUMS /////////////////////////////////////////////////////////////////
	// TYPES /////////////////////////////////////////////////////////////////
	// LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////
	// DATA MEMBERS //////////////////////////////////////////////////////////
		/** Borrowed reference of the Python object to process */
		PyObject* m_object;
};

/*@}*/
#endif
