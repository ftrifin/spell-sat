//////////////////////////////////////////////////////////////////////////////
/**
 *  \brief Wrapper for the SPELL database manager.
 *
 *  \par Description and usage:
 *
 *  	This class wraps the SPELL database manager instance existing on the
 *  	Python layer. It provides basic access to the most relevant methods
 *  	of the spell.lib.DatabaseManager class. This wrapper is implemented as a
 *  	singleton, and it is used in the SPELLexecutor class to load SPELL
 *      databases.
 *
 * \see SPELLexecutor, SPELLbase
 */
//////////////////////////////////////////////////////////////////////////////
class SPELLdatabaseManager
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		~SPELLdatabaseManager();

    // STATIC ////////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Obtain the singleton instance.
		 *
		 * \return The singleton instance.
		**////////////////////////////////////////////////////////////////////
		static SPELLdatabaseManager& instance();

    // METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Load builtin databases
		**////////////////////////////////////////////////////////////////////
		void loadBuiltinDatabases();

    // DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		**////////////////////////////////////////////////////////////////////
		SPELLdatabaseManager();

    // METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Access the DatabaseManager python class instance.
		 *
		 * \return The python object corresponding to the instance.
		**////////////////////////////////////////////////////////////////////
		PyObject* getDatabaseManagerObject();

    // DATA MEMBERS //////////////////////////////////////////////////////////

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////
    // DATA MEMBERS //////////////////////////////////////////////////////////
		/** Holds the database manager instance */
		PyObject* m_dbManager;
		/** Holds the singleton instance */
		static SPELLdatabaseManager* s_instance;
};

/*@}*/
#endif
