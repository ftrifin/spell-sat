#ifndef __SPELL_VARIABLE_BUFFER_H__
#define __SPELL_VARIABLE_BUFFER_H__

// FILES TO INCLUDE ////////////////////////////////////////////////////////
// System includes ---------------------------------------------------------
// Local includes ----------------------------------------------------------
// Project includes --------------------------------------------------------
#include "SPELL_EXC/SPELLvarInfo.H"
#include "SPELL_SYN/SPELLthread.H"
#include "SPELL_SYN/SPELLthread.H"
#include "SPELL_IPC/SPELLipcMessage.H"
#include "SPELL_SYN/SPELLmutex.H"

/** \addtogroup SPELL_CIFS */
/*@{*/


// FORWARD REFERENCES //////////////////////////////////////////////////////
class SPELLserverCif;
// TYPES ///////////////////////////////////////////////////////////////////
// DEFINES /////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Buffer for WOV messages
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLvariableBuffer : public SPELLthread
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		**////////////////////////////////////////////////////////////////////
		SPELLvariableBuffer( const std::string& procId, SPELLserverCif& cif );

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		~SPELLvariableBuffer();

    // METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Main thread method
		**////////////////////////////////////////////////////////////////////
		void run();

		//////////////////////////////////////////////////////////////////////
		/** Stop the buffer
		**////////////////////////////////////////////////////////////////////
		void stop();

		//////////////////////////////////////////////////////////////////////
		/** Send a variable update message
		**////////////////////////////////////////////////////////////////////
		void variableChange( const std::vector<SPELLvarInfo>& added,
				             const std::vector<SPELLvarInfo>& changed,
                             const std::vector<SPELLvarInfo>& deleted );

		//////////////////////////////////////////////////////////////////////
		/** Take care of scope changes: as scope changes do reset the WOV
		 * view entirely, there is no need to send any accumulated data
		**////////////////////////////////////////////////////////////////////
		void scopeChange();

		//////////////////////////////////////////////////////////////////////
		/** Collect the accumulated information and send the IPC message.
		**////////////////////////////////////////////////////////////////////
		void flush();

protected: //-----------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // STATIC ////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // STATIC ////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Check if data available
		**////////////////////////////////////////////////////////////////////
		bool hasDataToSend();

	// DATA MEMBERS //////////////////////////////////////////////////////////

		/** Holds the reference to the CIF */
		SPELLserverCif& m_cif;
		/** Working flag */
		bool m_working;
		/** Synchronization lock */
		SPELLmutex m_lock;
		/** Notification message skeletons */
		SPELLipcMessage m_variableMessage;
		/** List of added, updated and removed variables
		 * Note that it is a map, so updates overwrite:
		 * if a variable is added, then right away modified, then right away removed,
		 * only the removal effect will remain
		 * */
		std::map<std::string,SPELLvarInfo> m_variables;
		/** Accumulation of notifications */
		unsigned int m_total;
};

/*@}*/
#endif
