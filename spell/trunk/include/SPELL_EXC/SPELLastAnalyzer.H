// FORWARD REFERENCES //////////////////////////////////////////////////////
// TYPES ///////////////////////////////////////////////////////////////////
// DEFINES /////////////////////////////////////////////////////////////////
typedef std::map<unsigned int, unsigned int> LineTypes;

typedef struct NodeInfo_
{
	LineTypes lineTypes;
	unsigned int maxLineNo;
}
NodeInfo;

typedef std::map<std::string,NodeInfo> NodeMap;

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief AST code analyzer
 **
 ** \par Description:
 **
 ** \par Usage:
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLastAnalyzer
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		**////////////////////////////////////////////////////////////////////
		SPELLastAnalyzer();

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		~SPELLastAnalyzer();

	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Check if a line is simple or not
		**////////////////////////////////////////////////////////////////////
		bool isSimpleLine( unsigned int lineno );

		//////////////////////////////////////////////////////////////////////
		/** Check if a line is the start of a block
		**////////////////////////////////////////////////////////////////////
		bool isBlockStart( unsigned int lineno );

		//////////////////////////////////////////////////////////////////////
		/** Check if a line is inside of a block
		**////////////////////////////////////////////////////////////////////
		bool isInsideBlock( unsigned int lineno );

		//////////////////////////////////////////////////////////////////////
		/** Get the end of the block
		**////////////////////////////////////////////////////////////////////
		unsigned int getBlockEnd( unsigned int startLineNo );

		//////////////////////////////////////////////////////////////////////
		/** Process a file
		 *
		 * \param filename IN: path to filename to process
		 *
		**////////////////////////////////////////////////////////////////////
		void process( const std::string& filename );

    // DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Find relevant lines and line types in a node
		 *
		 * \param filename IN: path to filename to process
		 *
		**////////////////////////////////////////////////////////////////////
		void findNodeLines( struct _node* node, unsigned int depth, unsigned int& parDepth, NodeInfo& info );

	// DATA MEMBERS //////////////////////////////////////////////////////////

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////

		typedef enum _SPELLlineType
		{
			NONE = 0,
			START_MULTIPLE,
			MULTIPLE
		}
		SPELLlineType;

    // TYPES /////////////////////////////////////////////////////////////////

    // LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////
	// DATA MEMBERS //////////////////////////////////////////////////////////
		/** Last line number */
		unsigned int m_openLineNo;
		/** Last line type */
		SPELLlineType m_lastLineType;
		/** Holds the already processed AST code information structures */
		NodeMap m_nodes;
		/** Holds the info for the current AST code */
		NodeInfo m_currentNode;
};

/*@}*/
