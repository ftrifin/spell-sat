#ifndef __SPELL_IPC_CHANNEL_H__
#define __SPELL_IPC_CHANNEL_H__

// FILES TO INCLUDE ////////////////////////////////////////////////////////
// System includes ---------------------------------------------------------
// Local includes ----------------------------------------------------------
#include "SPELL_IPC/SPELLipcOutput.H"
#include "SPELL_IPC/SPELLipcInput.H"
// Project includes --------------------------------------------------------
#include "SPELL_SYN/SPELLevent.H"
#include "SPELL_SYN/SPELLmutex.H"


/** \addtogroup SPELL_IPC */
/*@{*/


// FORWARD REFERENCES //////////////////////////////////////////////////////
class SPELLipcInterface;
// TYPES ///////////////////////////////////////////////////////////////////
// DEFINES /////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Holds the information about an IPC client
 **
 ** \par Description and usage:
 **
 **  	USed by IPC SPELLipcServerInterface to hold the elements to interact
 **  	with a SPELLipcClientInterface which is on the other side of the
 **  	channel.
 **
 **  \see SPELLsocket, SPELLipcInput, SPELLipcOutput
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLipcChannel
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		 *
		 * \param name IN: name of the interface.
		 * \param socket IN: socket associated with the client
		 * \param key IN: assigned IPC key.
		 * \param ifc INOUT: parent server interface.
		**////////////////////////////////////////////////////////////////////
		SPELLipcChannel( const std::string& name, SPELLsocket* socket, int key, SPELLipcInterface& ifc );

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		virtual ~SPELLipcChannel();

    // STATIC ////////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Obtain the writer for this client.
		 *
		 * \return The output device for the client.
		**////////////////////////////////////////////////////////////////////
		SPELLipcOutput& getWriter() {
			return *m_output;
		};

		//////////////////////////////////////////////////////////////////////
		/** Obtain the reader for this client.
		 *
		 * \return The input device for the client.
		**////////////////////////////////////////////////////////////////////
		SPELLipcInput& getReader() {
			return *m_input;
		};

		//////////////////////////////////////////////////////////////////////
		/** Get the name of the channel
		 *
		 * \return The name of the channel
		**////////////////////////////////////////////////////////////////////
		std::string getName() {
			return m_name;
		};

		//////////////////////////////////////////////////////////////////////
		/** Get the socket
		 *
		 * \return The socket for the channel
		**////////////////////////////////////////////////////////////////////
		SPELLsocket& getSocket() {
			return *m_socket;
		};

		//////////////////////////////////////////////////////////////////////
		/** Get the IPC key
		 *
		 * \return The IPC key for the channel
		**////////////////////////////////////////////////////////////////////
		int getKey() {
			return m_key;
		};

		//////////////////////////////////////////////////////////////////////
		/** Connect the channel
		**////////////////////////////////////////////////////////////////////
		void connect();

		//////////////////////////////////////////////////////////////////////
		/** Check the channel is connected
		**////////////////////////////////////////////////////////////////////
		bool isConnected();

		//////////////////////////////////////////////////////////////////////
		/** Disconnect the channel
		**////////////////////////////////////////////////////////////////////
		void disconnect();

        //////////////////////////////////////////////////////////////////////
        /** Log a request as started
        **////////////////////////////////////////////////////////////////////
        void requestStarted( const std::string& reqId );

        //////////////////////////////////////////////////////////////////////
        /** Log a request as finished
        **////////////////////////////////////////////////////////////////////
        void requestFinished( const std::string& reqId );

        //////////////////////////////////////////////////////////////////////
        /** Get ongoing requests
        **////////////////////////////////////////////////////////////////////
        std::vector<std::string> getOngoingRequests();

    //////////////////////////////////////////////////////////////////////

    // DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

		friend class SPELLipcInput;
		friend class SPELLipcOutput;

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // STATIC ////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////

		virtual SPELLipcOutput* createOutput();
		virtual SPELLipcInput* createInput();

		void requestedEOC();
		void inputFailed( int errNo, const std::string& error );
		void outputFailed( int errNo, const std::string& error );

    // DATA MEMBERS //////////////////////////////////////////////////////////

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////

		void setConnected( bool connected );

    // DATA MEMBERS //////////////////////////////////////////////////////////

		/** Channel name */
		std::string m_name;
		/** Interface */
		SPELLipcInterface& m_ifc;
		/** Holds the client socket */
		SPELLsocket* m_socket;
		/** IPC key */
		int m_key;
		/** Connected flag */
		bool m_connected;
		/** Holds the input device */
		SPELLipcInput* m_input;
		/** Holds the output device */
		SPELLipcOutput* m_output;
		/** Holds the requests going out through this channel */
        std::vector<std::string> m_outgoingRequests;
        /** Sync lock */
        SPELLmutex m_dataLock;
};

/*@}*/
#endif
